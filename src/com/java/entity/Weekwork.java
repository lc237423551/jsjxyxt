package com.java.entity;

public class Weekwork {
	private String sno;
	private int weeknum;
	private String summary;//本周总结
	private String advise;//工作建议
	private String arrange;//下周安排
	private String deal;//老师对问题的处理
	private String evaluation;//教师的评价
	private String tab;//标记是否填写
	private String wtime;//最后修改的时间
	private Student student;
	public Weekwork(){}
	
	
	public Weekwork( int weeknum, String summary, String advise, String arrange, String deal,
			String evaluation) {
		super();
		this.weeknum = weeknum;
		this.summary = summary;
		this.advise = advise;
		this.arrange = arrange;
		this.deal = deal;
		this.evaluation = evaluation;
	}


	public Student getStudent() {
		return student;
	}
	public void setStudent(Student student) {
		this.student = student;
	}
	public String getSno() {
		return sno;
	}
	public void setSno(String sno) {
		this.sno = sno;
	}
	public int getWeeknum() {
		return weeknum;
	}
	public void setWeeknum(int weeknum) {
		this.weeknum = weeknum;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getAdvise() {
		return advise;
	}
	public void setAdvise(String advise) {
		this.advise = advise;
	}
	public String getArrange() {
		return arrange;
	}
	public void setArrange(String arrange) {
		this.arrange = arrange;
	}
	public String getDeal() {
		return deal;
	}
	public void setDeal(String deal) {
		this.deal = deal;
	}
	public String getEvaluation() {
		return evaluation;
	}
	public void setEvaluation(String evaluation) {
		this.evaluation = evaluation;
	}
	public String getTab() {
		return tab;
	}
	public void setTab(String tab) {
		this.tab = tab;
	}
	public String getWtime() {
		return wtime;
	}
	public void setWtime(String wtime) {
		this.wtime = wtime;
	}
	
}
